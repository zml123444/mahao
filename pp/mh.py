import socket#本机自带的模块
#tcp 协议
#服务器
#创建一个
f=socket.socket(socket.AF_INET,socket.SOCK_STREAM)
#绑定ip和端口号
f.bind(('192.168.2.134',531))
#监听，启用服务器以接受连接
f.listen(3)#数字代表最大的等待数
while True:
    #接受请求
    #第一个值，代表建立连接的信息
    #第二个值，代表客户端ip地址和端口号
    client,addr=f.accept()
    #接受客户端发送的请求最大1024个字节
    recive=client.recv(1024)
    #将接受的信息解码
    print(recive.decode('utf-8'))
    #输入响应的内容
    reponse=input('请响应')
    #将输入的响应内容发送
    client.send(reponse.encode('utf-8'))

